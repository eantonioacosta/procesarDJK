    private void botonEliminar() {
        int codigo = seleccionTablaCodigo();

        if (codigo != -1) {
            if (JOptionPane.showConfirmDialog(rootPane, "Seguro que desea eliminar el Docente?",
                    "AVISO", JOptionPane.WARNING_MESSAGE, JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                try {
                    control.eliminar(codigo);
                    JOptionPane.showMessageDialog(panelCentral, "Docente eliminado exitosamente.",
                            "Informacion", JOptionPane.INFORMATION_MESSAGE, frameIcon);
                    cargarTabla(0);
                } catch (NullPointerException ex) {
                    JOptionPane.showMessageDialog(panelCentral, ex.getMessage(), "Validacion", JOptionPane.QUESTION_MESSAGE);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(panelCentral, ex.getMessage(), "Excepcion",
                            JOptionPane.WARNING_MESSAGE);
                }

            }

        }
    }

    private void modificar() {
        try {
            Docente docente = obtenerCamposActualizar();
            control.actualizar(docente);
            JOptionPane.showMessageDialog(panelCentral, "Docente modificado exitosamente.",
                    "Informacion", JOptionPane.INFORMATION_MESSAGE);
            limpiarCampos();
            enableComponents(panelCreacion, false);
            cargarTabla(0);
        } catch (NullPointerException ex) {
            JOptionPane.showMessageDialog(panelCentral, ex.getMessage(), "Validacion", JOptionPane.QUESTION_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(panelCentral, ex.getMessage(), "Excepcion", JOptionPane.WARNING_MESSAGE);
        }
    }

    private void botonModificar() {
        int codigo = seleccionTablaCodigo();
        if (codigo != -1) {
            enableComponents(panelCreacion, true);
            btnGuardar.setText("MODIFICAR");
            textoId.setEnabled(false);
            pass.setEnabled(false);
            Docente docente;
            try {
                docente = control.buscarPorCodigo(codigo);
                pintarCampos(docente);
            } catch (NullPointerException ex) {
                JOptionPane.showMessageDialog(panelCentral, ex.getMessage(), "Validacion", JOptionPane.QUESTION_MESSAGE);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(panelCentral, ex.getMessage(), "Excepcion", JOptionPane.WARNING_MESSAGE);
            }

        }
    }

    private int seleccionTablaCodigo() {
        int indice = table.getSelectedRow();
        if (indice == -1) {
            JOptionPane.showMessageDialog(panelCentral, "Seleccione un Docente!", "Validacion", JOptionPane.INFORMATION_MESSAGE);
            return -1;
        } else {
            String codigo = String.valueOf(table.getValueAt(indice, 0));
            int cod = Integer.valueOf(codigo);
            return cod;
        }
    }

    private void botonGuardar() {
        if (validarCampos()) {
            Docente docente = obtenerCampos();
            try {
                control.registrar(docente);
                JOptionPane.showMessageDialog(panelCentral, "Docente registrado exitosamente.", "Informacion", JOptionPane.INFORMATION_MESSAGE);
                limpiarCampos();
                enableComponents(panelCreacion, false);
                cargarTabla(0);
            } catch (NullPointerException ex) {
                JOptionPane.showMessageDialog(panelCentral, ex.getMessage(), "Validacion", JOptionPane.QUESTION_MESSAGE);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(panelCentral, ex.getMessage(), "Excepcion", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(panelCentral, "Verifique los campos", "Validacion", JOptionPane.QUESTION_MESSAGE);
        }

    }

    private Docente obtenerCampos() {
        Docente docente;
        int codigo = Integer.parseInt(labelCodigo.getText());
        String identificacion = textoId.getText();
        String nombres = textoNombre.getText().toUpperCase();
        String apellidos = textoApellidos.getText().toUpperCase();
        String direccion = textoDireccion.getText();
        String correo = textoCorreo.getText();
        String telefono = textoTelefono.getText();
        String titulo = textoTitulo.getText();
        String ciudad = textoCiudad.getText();
        Long fecha = calendarFecha.getDate().getTime();
        //
        Date fechaNacimiento = new Date(fecha);
        int tipoSangre = comboTipoSangre.getSelectedIndex();
        int estado = comboEstado.getSelectedIndex();
        String password = String.valueOf(pass.getPassword());

        Usuario usuario = new Usuario(identificacion, nombres, password, 2);
        docente = new Docente(codigo, nombres, apellidos, estado, fechaNacimiento, tipoSangre, direccion,
                telefono, correo, ciudad, titulo, usuario);

        return docente;

    }

    private Docente obtenerCamposActualizar() {
        int codigo = Integer.parseInt(labelCodigo.getText());

        try {
            Docente docente;
            docente = control.buscarPorCodigo(codigo);
            docente.setNombres(textoNombre.getText().toUpperCase());
            docente.setApellidos(textoApellidos.getText().toUpperCase());
            docente.setDireccion(textoDireccion.getText());
            docente.setCorreo(textoCorreo.getText());
            docente.setTelefono(textoTelefono.getText());
            docente.setTitulo(textoTitulo.getText());
            docente.setCiudad(textoCiudad.getText());

            Long fecha = calendarFecha.getDate().getTime();
            Date fechaNacimiento = new Date(fecha);

            docente.setFechaNacimiento(fechaNacimiento);
            docente.setTipoSangre(comboTipoSangre.getSelectedIndex());
            docente.setEstado(comboEstado.getSelectedIndex());

            return docente;
        } catch (Exception ex) {
            Logger.getLogger(ViewDocente.class.getName()).log(Level.SEVERE, null, ex);
            return null;
        }
    }

